#include <stdio.h>

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}

int main(void)
{
	int	o;

	o = ft_isascii('p');
	printf("%d", o);
	return (0);
}
